package 变量类型;

public class bianlaingdemo {
    //类变量 独立于方法之外 用static修饰
    static String name = "qinghau";
    // 实例变量  独立于方法之外 不用static修饰
    String classname = "daxue";  // 实例变量

    public void show() {
        // 类的方法中的变量
        int age = 18; // 局部变量
        String name = "zhangsan";

    }
}
